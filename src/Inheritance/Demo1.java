package Inheritance;

public class Demo1 {


    public void m1() {

        System.out.println("This is m1 method of Demo1");
    }

    public void m2() {

        System.out.println("This is m2 method of Demo2");
    }


    public static void main(String args[]) {
        Demo1 obj = new Demo1();
        obj.m1();
        obj.m2();


    }
}

