package conditional_statements;

public class City {

    public static void main(String[] args) {

        String city="Pune";

        if(city=="AGRA"){

            System.out.println("Person belongs to Agra....");
        }
        if(city=="Delhi"){

            System.out.println("Person belongs to Delhi....");
        }
        if(city=="Jaipur"){

            System.out.println("Person belongs to Jaipur....");
        }
        if(city=="Pune"){

            System.out.println("Person belongs to Pune....");
        }


    }
}
