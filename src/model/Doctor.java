package model;

public class Doctor {

    String name;
    String collage;
    int year;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCollage() {
        return collage;
    }

    public void setCollage(String collage) {
        this.collage = collage;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public Doctor(String name, String collage, int year) {
        this.name = name;
        this.collage = collage;
        this.year = year;








    }
}
