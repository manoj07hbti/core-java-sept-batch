package Loops;

public class WhileLoop {
    public static void main(String args[]){
        int i=1;

        while(i<=12){
            System.out.println("Reynolds Pen" +i);
            i++;
        }

        i=30;

        while(i>=1){
            System.out.println("Apsara Pencil Bolt" +i);
            i--;
        }

    }
}
